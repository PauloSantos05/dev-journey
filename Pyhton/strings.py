name = 'Neo Matrix'

print('len: ', len(name))  # length of the string
print('find: ', name.find('o'))  # find the index of the character
print('capitalize: ', name.capitalize())  # capitalize the first letter
print('upper: ', name.upper())  # convert to uppercase
print('lower: ', name.lower())  # convert to lowercase
print('replace: ', name.replace('o', '0'))  # replace characters
print('split: ', name.split('e'))  # split the string by a character
print('isDigit: ', name.isdigit())  # check if the string is a digit
print('isAlpha: ', name.isalpha())  # check if the string is alphabetic
print('isAlnum: ', name.isalnum())  # check if the string is alphanumeric
print('strip: ', name.strip())  # remove leading and trailing whitespace
print('count: ', name.count('a'))  # count occurrences of a character
print('last character: ', name[-1])  # access the last character
print('first character: ', name[0])  # access the first character